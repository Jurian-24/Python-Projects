def circle(diameter, angle, limit):
    reverseDirection = 300
    jurian.forward(diameter)
    diameter = diameter - 1
    if diameter % (reverseDirection*2) == 0:
        angle = angle - 2
    elif diameter % reverseDirection == 0:
        angle = angle + 2

    jurian.right(angle)
    diameter = diameter + 4
    jurian.circle(diameter)
    
    circle(diameter, angle, limit)




